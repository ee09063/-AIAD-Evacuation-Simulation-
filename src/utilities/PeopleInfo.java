package utilities;

public class PeopleInfo {

	public int x;
	public int y;
	public int pc;
	public double pl;
	public int lh;
	public boolean kn;
	
	public PeopleInfo(int x, int y, int pc, double pl, int lh, boolean kn){
		this.x = x;
		this.y = y;
		this.pc = pc;
		this.pl = pl;
		this.lh = lh;
		this.kn = kn;
	}
}
